clear; clc; close all;

if ~exist('mpc_full_closed_loop_traj.mat','file')
    error('Cannot find mpc_full_closed_loop_traj.mat. Run the training file first.');
end

load('mpc_full_closed_loop_traj.mat');

%% ================== style ==================
font_Name = 'Times New Roman';

figure_width    = 5.0;
figure_hight    = 3.6;
figure_FontSize = 7;
line_Width      = 1.1;
axis_Width      = 1.0;
legendLW        = 0.6;
figure_Name     = 'mpc_full_closed_loop_traj';

targetColor   = [0.10 0.10 0.10];
beforeColor   = [0.05 0.05 0.95];
afterColor    = [0.95 0.05 0.05];
obstacleColor = [0, 128, 0] / 256;
gridColor     = [0.82 0.82 0.82];

th_plot = linspace(0, 2*pi, 400);

set(groot,'defaultTextInterpreter','latex');
set(groot,'defaultAxesTickLabelInterpreter','latex');
set(groot,'defaultLegendInterpreter','tex');

fig = figure('Color','w');
set(fig,'unit','centimeters','position',[5,5,figure_width,figure_hight]);
hold on; box on; axis equal;

%% ================== obstacle circles ==================
% actual obstacle: r = 0.4
plot(circle_eval(1) + r_real*cos(th_plot), ...
     circle_eval(2) + r_real*sin(th_plot), ...
     '-', ...
     'Color', obstacleColor, ...
     'LineWidth', 1.8, ...
     'HandleVisibility','off');

plot(circle_eval(1), circle_eval(2), ...
     '+', ...
     'Color', obstacleColor, ...
     'LineWidth', 1.2, ...
     'MarkerSize', 5, ...
     'HandleVisibility','off');

% before training radius
plot(circle_eval(1) + r_before*cos(th_plot), ...
     circle_eval(2) + r_before*sin(th_plot), ...
     '--', ...
     'Color', beforeColor, ...
     'LineWidth', 1.15, ...
     'HandleVisibility','off');

% after training radius
plot(circle_eval(1) + r_after*cos(th_plot), ...
     circle_eval(2) + r_after*sin(th_plot), ...
     '--', ...
     'Color', afterColor, ...
     'LineWidth', 1.15, ...
     'HandleVisibility','off');

%% ================== trajectories ==================
h1 = plot(X_demo_full(1,:), X_demo_full(2,:), ...
     '--', ...
     'Color', targetColor, ...
     'LineWidth', 1.05*line_Width);

h2 = plot(X_before_full(1,:), X_before_full(2,:), ...
     '-', ...
     'Color', beforeColor, ...
     'LineWidth', 1.15*line_Width);

h3 = plot(X_after_full(1,:), X_after_full(2,:), ...
     '-', ...
     'Color', afterColor, ...
     'LineWidth', 1.20*line_Width);

%% ================== start / goal ==================
plot(X_demo_full(1,1), X_demo_full(2,1), ...
     'ko', ...
     'MarkerFaceColor', 'k', ...
     'MarkerSize', 3.5, ...
     'HandleVisibility','off');

plot(xref_eval(1), xref_eval(2), ...
     'mx', ...
     'LineWidth', 0.9, ...
     'MarkerSize', 6, ...
     'HandleVisibility','off');

xlabel('$p_x$', 'FontName', font_Name, 'FontSize', figure_FontSize);
ylabel('$p_y$', 'FontName', font_Name, 'FontSize', figure_FontSize);

ax = gca;
set(ax, 'FontName', font_Name, ...
        'FontSize', figure_FontSize, ...
        'LineWidth', axis_Width, ...
        'Box', 'on', ...
        'Layer', 'bottom', ...
        'TickDir', 'in', ...
        'TickLength', [0.015 0.015], ...
        'XGrid', 'on', ...
        'YGrid', 'on', ...
        'GridColor', gridColor, ...
        'GridAlpha', 0.9, ...
        'MinorGridAlpha', 0.9);

lgd = legend([h1,h2,h3], ...
    {'target','init','trained'}, ...
    'Location', 'southeast', ...
    'Box', 'on', ...
    'Interpreter', 'tex', ...
    'FontName', font_Name, ...
    'FontSize', 6.5);
lgd.LineWidth = legendLW;

set(fig,'PaperUnits','centimeters');
set(fig,'PaperPosition',[0,0,figure_width,figure_hight]);
set(fig,'PaperSize',[figure_width,figure_hight]);

print(fig, figure_Name, '-dpdf', '-painters');
print(fig, [figure_Name '.svg'], '-dsvg', '-painters');